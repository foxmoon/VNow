package com.nyist.vnow.struct;

import com.j256.ormlite.field.DatabaseField;

public class Conference {
    @DatabaseField(id = true)
    public String uuid;
    @DatabaseField
    public String confName;
    @DatabaseField
    public String confTheme;
    @DatabaseField
    public String confTime;

    public String getmConfName() {
        return confName;
    }

    public void setmConfName(String mConfName) {
        this.confName = mConfName;
    }

    public String getmConfTheme() {
        return confTheme;
    }

    public void setmConfTheme(String mConfTheme) {
        this.confTheme = mConfTheme;
    }

    public String getmConfTime() {
        return confTime;
    }

    public void setmConfTime(String mConfTime) {
        this.confTime = mConfTime;
    }
}
