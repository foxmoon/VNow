package com.nyist.vnow.exception;

/**
 * @author harry
 * @version Creat on 2014-6-26上午11:59:12
 */
public class UrlException extends Exception {
    private static final long serialVersionUID = 1L;

    public UrlException() {}

    public UrlException(String detailMessage) {
        super(detailMessage);
    }

    public UrlException(Throwable throwable) {
        super(throwable);
    }

    public UrlException(String detailMessage, Throwable throwable) {
        super(detailMessage, throwable);
    }
}
